export function getCombinedProfileByCode(code: string): CombinedProfile | undefined {
  return combinedProfiles.find((p) => p.code === code);
}

export function getCombinedProfile(
  skinShadeNumber: number,
  hairCode: string,
  eyeCode: string
): CombinedProfile | undefined {
  return combinedProfiles.find(
    (p) =>
      p.skin_shade_number === skinShadeNumber &&
      p.hair_code === hairCode &&
      p.eye_code === eyeCode
  );
}

export function getCombinedProfilesForSkin(skinShadeNumber: number): CombinedProfile[] {
  return combinedProfiles.filter((p) => p.skin_shade_number === skinShadeNumber);
}
